package main

import "fmt"

func main() {
	a := 10
	b := 20

	//Comparison Operators
	fmt.Println(a == b) //==	Equal Operator
	fmt.Println(a != b) //!=	Not equal Operator
	fmt.Println(a < b)  // <	Less than Operator
	fmt.Println(a <= b) // <	Less than equal Operator
	fmt.Println(a > b)  // >	Greater than Operator
	fmt.Println(a >= b) // >	Greater than  equal Operator

}
